-- Минимальный учебный STDL-скрипт только про построение 3D-геометрии.
-- Здесь мы специально не делаем:
-- - символику;
-- - порты;
-- - SetVisible();
-- - дополнительные инженерные свойства.
--
-- Наша цель:
-- 1. взять размеры из JSON;
-- 2. по точкам построить 2D-профиль;
-- 3. выдавить профиль в глубину через Extrude();
-- 4. передать результат в SetDetailedGeometry().

-- Получаем все значения параметров из geometry.json.
local parameters = Style.GetParameterValues()

-- Читаем размеры из группы Dimensions.
local baseWidth = parameters.Dimensions.BaseWidth
local shoulderHeight = parameters.Dimensions.ShoulderHeight
local topWidth = parameters.Dimensions.TopWidth
local totalHeight = parameters.Dimensions.TotalHeight
local depth = parameters.Dimensions.Depth

-- Для построения симметричного профиля удобно сразу посчитать половины ширин.
local halfBaseWidth = baseWidth / 2
local halfTopWidth = topWidth / 2

-- Список точек профиля.
-- Профиль строится во фронтальной плоскости XZ, но в CreatePolyline2D
-- мы задаем его как 2D-кривую с координатами (X, Z).
--
-- Ниже фигура обходится по контуру по часовой стрелке:
--
-- 1. левая нижняя точка основания
-- 2. правая нижняя точка основания
-- 3. правая верхняя точка прямой боковины
-- 4. правая верхняя точка суженной части
-- 5. левая верхняя точка суженной части
-- 6. левая верхняя точка прямой боковины
-- 7. снова первая точка, чтобы замкнуть контур
local profilePoints = {
    Point2D(-halfBaseWidth, 0),
    Point2D(halfBaseWidth, 0),
    Point2D(halfBaseWidth, shoulderHeight),
    Point2D(halfTopWidth, totalHeight),
    Point2D(-halfTopWidth, totalHeight),
    Point2D(-halfBaseWidth, shoulderHeight),
    Point2D(-halfBaseWidth, 0)
}

-- Создаем полилинию по этим точкам.
-- Так как первая и последняя точки совпадают, контур получается замкнутым.
local profile = CreatePolyline2D(profilePoints)

-- Placement3D задает, где этот 2D-профиль будет расположен перед выдавливанием.
-- Если говорить совсем по-простому, Placement3D отвечает сразу за три вопроса:
-- 1. где стоит профиль;
-- 2. куда пойдет выдавливание;
-- 3. где у профиля "право".
--
-- Point3D(0, 0, 0):
-- начало локальной системы координат профиля ставится в мировую точку (0, 0, 0).
-- Это НЕ центрирует тело автоматически. Это просто точка, от которой строится профиль
-- и в сторону которой потом считается выдавливание.
--
-- Vector3D(0, 1, 0):
-- локальная ось Z профиля направлена вдоль мировой оси Y.
-- Именно вдоль этой оси и произойдет Extrude().
--
-- Vector3D(1, 0, 0):
-- локальная ось X профиля совпадает с мировой осью X.
--
-- Важный нюанс:
-- у профиля есть еще локальная ось Y, и она вычисляется из двух заданных осей.
-- В текущем примере она получается направленной в сторону мировой -Z.
-- Поэтому в Renga положительные значения второй координаты Point2D(x, z)
-- визуально уходят вниз, а не вверх.
--
-- То есть на плоской учебной схеме мы читаем профиль так:
-- - z = 0  -> низ фигуры;
-- - z = 100 -> верх фигуры.
--
-- А в реальной 3D-модели после placement-а получается:
-- - меньшая вторая координата Point2D оказывается выше;
-- - большая вторая координата Point2D оказывается ниже.
--
-- Из-за этого:
-- - точка Point2D(..., 0) оказывается "выше";
-- - точка Point2D(..., totalHeight) оказывается "ниже";
-- - поэтому объект в модели выглядит так, как будто его широкая часть смотрит вверх.
local placement = Placement3D(
    Point3D(0, 0, 0),
    Vector3D(0, 1, 0),
    Vector3D(1, 0, 0)
)
-- Выдавливаем профиль на заданную глубину.
-- ExtrusionParameters(depth) означает: построить тело длиной depth
-- вдоль локальной оси Z placement-а.
--
-- Важно:
-- сам Extrude не решает, куда именно в мировом пространстве растить тело.
-- Он смотрит на placement и берет из него направление локальной оси выдавливания.
local solid = Extrude(profile, ExtrusionParameters(depth), placement)

-- Создаем контейнер детальной 3D-геометрии.
local detailedGeometry = ModelGeometry()

-- Добавляем туда полученное тело.
detailedGeometry:AddSolid(solid)

-- Передаем геометрию в Renga.
Style.SetDetailedGeometry(detailedGeometry)
