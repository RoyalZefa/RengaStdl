]]

-- Как читать этот файл:
-- `Типовой паттерн STDL из документации Renga`:
-- получение параметров, создание `ModelGeometry()`, вызовы `Style.SetDetailedGeometry(...)`,
-- `Style.SetSymbolGeometry(...)`, `Style.SetSymbolicGeometry(...)`, использование `Placement3D(...)`.
-- `Наша логика проекта`:
-- конкретные имена параметров, размеры и форма именно этого учебного объекта.
-- В этом шаблоне своей логики почти нет: он специально показывает минимальный каркас,
-- который удобно брать за основу нового проекта.

-- Получаем все значения параметров из `parameters.json`.
--
-- Это почти всегда первая строка логики STDL-шаблона.
-- После нее у нас появляется объект `parameters`,
-- в котором можно читать значения по группам и именам параметров.
local parameters = Style.GetParameterValues()

-- Читаем три базовых размера объекта.
--
-- Эти параметры объявлены в JSON в группе `Dimensions`.
-- Имена должны совпадать полностью:
-- - `Width`
-- - `Depth`
-- - `Height`
local width = parameters.Dimensions.Width
local depth = parameters.Dimensions.Depth
local height = parameters.Dimensions.Height

-- Создаем контейнер для детальной 3D-геометрии.
--
-- Детальная геометрия - это "настоящее" тело объекта,
-- которое пользователь увидит в модели.
local detailedGeometry = ModelGeometry()

-- Добавляем в него самый простой 3D-примитив - блок.
--
-- Это минимальный рабочий пример:
-- объект с тремя размерами и прямоугольной формой.
detailedGeometry:AddSolid(CreateBlock(width, depth, height))

-- Передаем детальную геометрию в Renga.
-- Без этого тело будет создано в коде, но не станет геометрией стиля.
Style.SetDetailedGeometry(detailedGeometry)

-- Выносим создание 2D-символа в отдельную функцию.
--
-- Это хорошая привычка даже для очень простых проектов:
-- потом такую функцию легко дорабатывать, не смешивая 2D и 3D-логику.
local function createTopSymbol()
    local geometry = GeometrySet2D()

    -- Строим простой прямоугольник по ширине и глубине объекта.
    -- Это вид сверху.
    geometry:AddCurve(CreateRectangle2D(Point2D(0, 0), 0, width, depth))

    return geometry
end

-- Создаем компактный символ стиля.
--
-- Обычно это маленькое условное представление объекта.
-- В простых проектах можно использовать ту же геометрию, что и для вида сверху.
local symbolGeometry = ModelGeometry()
symbolGeometry:AddGeometrySet2D(createTopSymbol())
Style.SetSymbolGeometry(symbolGeometry)

-- Создаем условную геометрию объекта.
--
-- Это не обязательно та же самая геометрия, что и `SetSymbolGeometry`,
-- но в простом шаблоне их можно делать одинаковыми.
local symbolicGeometry = ModelGeometry()

-- Размещаем 2D-символ на верхней плоскости объекта.
--
-- Почему используется `height`:
-- потому что символ кладется на верхнюю грань блока.
--
-- Ось Z смотрит вверх.
-- Ось X смотрит вправо.
local topPlacement = Placement3D(
    Point3D(0, 0, height),
    Vector3D(0, 0, 1),
    Vector3D(1, 0, 0)
)

-- Добавляем 2D-символ в условную геометрию с нужным размещением.
symbolicGeometry:AddGeometrySet2D(createTopSymbol(), topPlacement)

-- Передаем условную геометрию в Renga.
Style.SetSymbolicGeometry(symbolicGeometry)
