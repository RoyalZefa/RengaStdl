Это эталонная минимальная заготовка нового STDL-проекта.

Зачем нужен этот файл:
- показать самый короткий рабочий каркас STDL;
- дать основу, от которой можно быстро стартовать новый объект;
- объяснить, какие части почти всегда присутствуют в любом шаблоне.

Важно:
- это учебная версия файла;
- рабочая сборка использует обычный `main.lua`;
